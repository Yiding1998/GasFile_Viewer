#!/usr/bin/env python3
"""Build single-file offline workbenches from the shared web sources."""

from __future__ import annotations

import argparse
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
DEPENDENCIES = (
    "assets/js/gas-file-parser.js",
    "assets/js/gas-search-core.js",
    "assets/js/workbench-library.js",
)
WORKBENCHES = {
    "garfield_gas_workbench_pro.html": "garfield_workbench_offline_zh.html",
    "garfield_gas_workbench_pro_english.html": (
        "garfield_workbench_offline_en.html"
    ),
}
NOTICE = "<!-- Generated by tools/build_standalone_workbenches.py; do not edit directly. -->"


def inline_script(name: str) -> str:
    script = (ROOT / name).read_text(encoding="utf-8")
    # A literal closing script tag would terminate the surrounding HTML element.
    script = script.replace("</script", r"<\/script")
    return f'<script data-inlined-source="{name}">\n{script}\n</script>'


def render(source_name: str) -> str:
    html = (ROOT / source_name).read_text(encoding="utf-8")
    if NOTICE in html:
        raise ValueError(f"{source_name} is already a generated workbench")
    for dependency in DEPENDENCIES:
        marker = f'<script src="{dependency}"></script>'
        if html.count(marker) != 1:
            raise ValueError(f"Expected exactly one {marker} in {source_name}")
        html = html.replace(marker, inline_script(dependency))
    first_line, remainder = html.split("\n", 1)
    return f"{first_line}\n{NOTICE}\n{remainder}"


def build(check: bool) -> int:
    stale: list[str] = []
    for source_name, output_name in WORKBENCHES.items():
        output = ROOT / output_name
        expected = render(source_name)
        if check:
            if not output.exists() or output.read_text(encoding="utf-8") != expected:
                stale.append(output_name)
            continue
        output.write_text(expected, encoding="utf-8")
        print(f"Wrote {output}")
    if stale:
        print("Standalone workbenches are stale: " + ", ".join(stale))
        print("Run: python3 tools/build_standalone_workbenches.py")
        return 1
    if check:
        print(f"Standalone workbenches are current ({len(WORKBENCHES)} files).")
    return 0


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--check", action="store_true", help="verify generated files")
    return build(parser.parse_args().check)


if __name__ == "__main__":
    raise SystemExit(main())
